const MessageInfo = () => {
  const messageId = useAssistantState(s => s.message.id);
  const role = useAssistantState(s => s.message.role);
  const dateValue = useAssistantState(s => {
    // cast to any to safely access optional/variant timestamp fields that may exist
    const msg = s.message as any;
    // try common fields in order: sentAt, metadata.createdAt, metadata.created_at, createdAt, timestamp
    return (
      msg.sentAt ??
      msg.metadata?.createdAt ??
      msg.metadata?.created_at ??
      msg.createdAt ??
      msg.timestamp ??
      Date.now()
    );
  });
  const date = new Date(dateValue);
  const now = new Date();
  const isToday = date.toDateString() === now.toDateString();
  const isYesterday = date.toDateString() === new Date(now.getTime() - 86400000).toDateString();
  
  let formattedDate: string;
  
  if (isToday) {
    formattedDate = new Intl.DateTimeFormat("es-ES", {
      hour: "2-digit",
      minute: "2-digit",
    }).format(date);
    formattedDate = `Hoy, ${formattedDate}`;
  } else if (isYesterday) {
    formattedDate = new Intl.DateTimeFormat("es-ES", {
      hour: "2-digit",
      minute: "2-digit",
    }).format(date);
    formattedDate = `Ayer, ${formattedDate}`;
  } else {
    formattedDate = new Intl.DateTimeFormat("es-ES", {
      day: "numeric",
      month: "short",
      hour: "2-digit",
      minute: "2-digit",
    }).format(date);
  }

  return (
    <div className="text-xs text-muted-foreground/70 mt-1.5 text-right font-normal">
      {formattedDate}
    </div>
  );
};

